# [KR] 이름과 성을 각각 문자열 변수에 저장합니다.
# [JP] 名前と苗字をそれぞれ文字列変数に格納します。
first_name = "John"
last_name = "Doe"

# [KR] 방법 1: '+' 연산자를 사용하여 두 문자열 사이에 공백(" ")을 넣고 연결합니다.
# [JP] 方法 1: '+' 演算子を使用して、2つの文字列の間にスペース(" ")を入れて連結します。
print(first_name + " " + last_name)

# [KR] 방법 2 (권장): f-string을 사용하면 더 직관적으로 문자열을 조합할 수 있습니다.
# [KR] 최근 파이썬에서는 f-string을 더 선호하는 경향이 있습니다.
# [JP] 方法 2 (推奨): f-stringを使うと、より直感的に文字列を組み合わせることができます。
# [JP] 最近のPythonでは、f-stringが好まれる傾向にあります。
print(f"{first_name} {last_name}")

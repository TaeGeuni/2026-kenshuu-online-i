# [KR] input() 함수를 사용하여 사용자로부터 나이를 문자열 형태로 입력받습니다.
# [JP] input()関数を使用して、ユーザーから年齢を文字列として入力してもらいます。
age_input = input("年齢を入力してください: ")

# [KR] 입력받은 문자열 데이터를 정수형(int)으로 변환합니다. (형 변환)
# [JP] 入力された文字列データを整数型(int)に変換します。（キャスト）
age_int = int(age_input)

# [KR] 변환된 나이와 해당 데이터 타입을 출력합니다.
# [JP] 変換された年齢とそのデータ型を出力します。
print(f"入力された年齢: {age_int}")
print(f"変換後の型: {type(age_int)}")

# [KR] 나이가 20세 이상인지 비교 연산자(>=)로 확인하여 불리언 값(True/False)을 구합니다.
# [JP] 年齢が20歳以上であるかを比較演算子(>=)で確認し、ブール値(True/False)を求めます。
is_over_20 = age_int >= 20

# [KR] 최종적으로 20세 이상 여부를 출력합니다.
# [JP] 最終的に、20歳以上であるかどうかを出力します。
print(f"20歳以上ですか?: {is_over_20}")

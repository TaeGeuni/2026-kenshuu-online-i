# [KR] 동일한 요소를 가진 두 개의 리스트를 각각 생성합니다.
# [JP] 同じ要素を持つ2つのリストをそれぞれ作成します。
a = [1, 2, 3]
b = [1, 2, 3]

# [KR] '==' 연산자는 두 객체의 '값(Value)'이 같은지 비교합니다. (값이 같으므로 True)
# [JP] '==' 演算子は2つのオブジェクトの「値(Value)」が同じかどうかを比較します。（値が同じなので True）
print(f"a == b: {a == b}")

# [KR] 'is' 연산자는 두 객체가 메모리상에서 '완전히 동일한 객체(Identity)'인지 비교합니다.
# [JP] 'is' 演算子は2つのオブジェクトがメモリ上で「完全に同一のオブジェクト(Identity)」であるかを比較します。
# 💡Point: a와 b는 값은 같지만, 메모리상에서는 서로 다른 별개의 리스트이므로 False가 출력됩니다.
# 💡Point: aとbは値は同じですが、メモリ上では互いに異なる別々のリストであるため False が出力されます。
print(f"a is b: {a is b}")


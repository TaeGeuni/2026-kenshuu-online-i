# [KR] 문제에 주어진 두 개의 정수를 변수 a와 b에 대입합니다.
# [JP] 問題で与えられた2つの整数を変数aとbに代入します。
a = 17
b = 5

# [KR] 일반 나눗셈 (/): 결과를 소수점까지 정확히 계산합니다. (float 반환)
# [JP] 通常除算 (/): 結果を小数点まで正確に計算します。（floatを返す）
normal_div = a / b

# [KR] 버림 나눗셈 (//): 나눗셈 결과의 소수점 이하를 버리고 몫(정수 부분)만 구합니다.
# [JP] 切り捨て除算 (//): 割り算の結果の小数点以下を切り捨て、商（整数部分）のみを求めます。
floor_div = a // b

# [KR] 나머지 연산 (%): a를 b로 나눈 후의 나머지 값을 구합니다. (홀수/짝수 판별 등에 자주 쓰임)
# [JP] 剰余 (%): aをbで割った後の余りを求めます。（奇数/偶数の判定などによく使われます）
mod = a % b

# [KR] f-string을 사용하여 요구사항에 맞게 계산 결과를 설명과 함께 출력합니다.
# [JP] f-stringを使用して、要件に合わせて計算結果を説明とともに出力します。
print(f"通常除算 (a / b): {normal_div}")
print(f"切り捨て除算 (a // b): {floor_div}")
print(f"剰余 (a % b): {mod}")

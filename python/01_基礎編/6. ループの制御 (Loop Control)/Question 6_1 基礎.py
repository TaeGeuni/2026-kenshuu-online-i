# [KR] 1부터 10까지 반복하기 위해 range(1, 11)을 사용합니다.
# [JP] 1から10まで繰り返すために range(1, 11) を使用します。
for i in range(1, 11):
    # [KR] 문제의 요구사항에 따라 먼저 숫자를 출력합니다.
    # [JP] 問題の要件に従って、まず数値を出力します。
    print(i)

    # [KR] 출력한 숫자가 5라면 break를 실행하여 루프를 즉시 강제 종료합니다.
    # [JP] 出力した数値が5であれば、breakを実行してループを即座に強制終了します。
    if i == 5:
        break

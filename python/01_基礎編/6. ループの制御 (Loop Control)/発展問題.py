# [KR] 1부터 20까지의 숫자를 순회합니다.
# [JP] 1から20までの数値を順回します。
for i in range(1, 21):
    # [KR] 7로 나누어 떨어지는 수(7의 배수)를 찾습니다.
    # [JP] 7で割り切れる数（7の倍数）を探します。
    if i % 7 == 0:
        print(f"最初に見つかった7の倍数: {i}")
        # [KR] 찾았으므로 더 이상 반복할 필요 없이 루프를 빠져나갑니다.
        # [JP] 見つけたので、これ以上繰り返す必要はなくループを抜けます。
        break

# 💡Point: for문과 같은 들여쓰기 라인에 else를 작성합니다!
# 💡Point: for文と同じインデントラインにelseを記述します！
else:
    # [KR] for...else 구문: 루프 도중 'break'를 한 번도 만나지 않고 끝까지 정상 종료되었을 때만 실행됩니다.
    # [JP] for...else 構文: ループの途中で 'break' に一度も遭遇せず、最後まで正常に終了した場合のみ実行されます。
    print("7の倍数は見つかりませんでした")

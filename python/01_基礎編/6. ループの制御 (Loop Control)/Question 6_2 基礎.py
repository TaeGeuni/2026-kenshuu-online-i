# [KR] 1부터 10까지 반복합니다.
# [JP] 1から10まで繰り返します。
for i in range(1, 11):
    # [KR] 현재 숫자(i)가 짝수(2로 나누어 떨어지는 수)인지 확인합니다.
    # [JP] 現在の数値(i)が偶数（2で割り切れる数）かどうかを確認します。
    if i % 2 == 0:
        # [KR] 짝수라면 continue를 만나 아래의 print(i)를 실행하지 않고, 다음 숫자로 넘어갑니다(스킵).
        # [JP] 偶数であれば、continueによって下の print(i) を実行せずに、次の数値に進みます（スキップ）。
        continue

    # [KR] 짝수가 아닌 경우(홀수)에만 이 코드가 실행되어 출력됩니다.
    # [JP] 偶数ではない場合（奇数）のみ、このコードが実行されて出力されます。
    print(i)

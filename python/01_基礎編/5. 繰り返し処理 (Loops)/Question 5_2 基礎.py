# [KR] while 루프의 기준이 될 초기 변수(카운터)를 설정합니다.
# [JP] whileループの基準となる初期変数（カウンター）を設定します。
count = 1

# [KR] count가 10 이하일 때까지(10이 될 때까지) 아래의 코드를 반복해서 실행합니다.
# [JP] countが10以下である間（10になるまで）、以下のコードを繰り返し実行します。
while count <= 10:
    print(count)
    # [KR] 무한 루프에 빠지지 않도록 반복이 끝날 때마다 count를 1씩 증가시킵니다.
    # [JP] 無限ループに陥らないように、繰り返しが終わるたびにcountを1ずつ増やします。
    count += 1

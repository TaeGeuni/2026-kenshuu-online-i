# [KR] 테스트를 위한 변수 num을 선언합니다. (-5와 0으로 번갈아 테스트해 보세요)
# [JP] テストのための変数numを宣言します。（-5と0で交互にテストしてみてください）
num = -5

# [KR] if-elif-else 문을 사용하여 양수, 음수, 0을 판별합니다.
# [JP] if-elif-else文を使用して、正の数、負の数、ゼロを判定します。
if num > 0:
    print(f"{num}は正の数です。")
elif num < 0:
    print(f"{num}は負の数です。")
else:
    # [KR] 0보다 크지도, 작지도 않다면 무조건 0입니다.
    # [JP] 0より大きくも小さくもなければ、必ずゼロです。
    print(f"{num}は正の数でも負の数でもありません。")

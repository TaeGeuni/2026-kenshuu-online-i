# [KR] 테스트를 위한 변수 num에 7을 대입합니다.
# [JP] テストのための変数numに7を代入します。
num = 7

# [KR] 파이썬에서는 '5 <= num <= 10' 처럼 직관적인 범위 비교가 가능합니다!
# [JP] Pythonでは '5 <= num <= 10' のように直感的な範囲比較が可能です！
if num > 10:
    print(f"{num}は10より大きいです。")
elif 5 <= num <= 10:
    print(f"{num}は5以上10以下です。")
else:
    # [KR] 위의 두 조건에 모두 해당하지 않으면 자연스럽게 5 미만이 됩니다.
    # [JP] 上記の2つの条件のどちらにも当てはまらない場合、自然に5未満になります。
    print(f"{num}は5未満です。")

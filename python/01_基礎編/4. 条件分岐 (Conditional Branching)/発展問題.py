# [KR] 판별하고자 하는 연도(year)를 대입합니다. (2024, 2026 등으로 변경하며 테스트해보세요)
# [JP] 判定したい年(year)を代入します。（2024、2026などに変更してテストしてみてください）
year = 2024

# [KR] 윤년 조건: (4로 나누어 떨어지고 'AND' 100으로 나누어 떨어지지 않음) 'OR' (400으로 나누어 떨어짐)
# [JP] うるう年の条件: (4で割り切れる 'AND' 100で割り切れない) 'OR' (400で割り切れる)
if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
    print(f"{year}年はうるう年です。")
else:
    print(f"{year}年はうるう年ではありません。")

# [KR] 변수 num에 12를 대입하고 조건을 판별합니다.
# [JP] 変数numに12を代入し、条件を判定します。
num1 = 12

# [KR] 조건: 짝수(2로 나누어 떨어짐) '그리고(and)' 10보다 큼
# [JP] 条件: 偶数（2で割り切れる）「かつ(and)」10より大きい
condition1 = (num1 % 2 == 0) and (num1 > 10)
print(f"{num1}は条件を満たしますか？: {condition1}")


# [KR] 변수 num에 8을 대입하고 같은 조건을 판별합니다.
# [JP] 変数numに8を代入し、同じ条件を判定します。
num2 = 8

# [KR] 8은 짝수지만 10보다 크지 않으므로 False가 됩니다.
# [JP] 8は偶数ですが10より大きくないため、Falseになります。
condition2 = (num2 % 2 == 0) and (num2 > 10)
print(f"{num2}は条件を満たしますか？: {condition2}")

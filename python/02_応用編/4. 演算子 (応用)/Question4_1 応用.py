# [KR] input()으로 상품의 가격을 입력받아, 계산을 위해 정수(int)로 변환합니다.
# [JP] input()で商品の値段を入力してもらい、計算のために整数(int)に変換します。
price = int(input("商品の値段を入力してください: "))

# [KR] 입력받은 가격을 그대로 한 번 출력합니다.
# [JP] 入力された値段をそのまま一度出力します。
print(price)

# [KR] 가격에 0.7을 곱하여 30% 할인가를 구하고, int()를 사용해 소수점 이하를 버립니다.
# [JP] 値段に0.7を掛けて3割引の価格を計算し、int()を使って小数点以下を切り捨てます。
discounted_price = int(price * 0.7)

# [KR] 최종 계산된 할인 가격을 출력합니다.
# [JP] 最終的に計算された割引価格を出力します。
print(f"3割引きで{discounted_price}円です")

# [KR] 3번의 입력을 각각 다른 변수에 저장합니다.
# [JP] 3回の入力をそれぞれ別の変数に保存します。
word1 = input("1つ目の言葉を入力: ")
word2 = input("2つ目の言葉を入力: ")
word3 = input("3つ目の言葉を入力: ")

# [KR] 각각의 단어를 먼저 한 줄씩 출력합니다.
# [JP] それぞれの言葉をまず1行ずつ出力します。
print(word1)
print(word2)
print(word3)

# [KR] 3개의 문자열을 '+' 연산자로 하나로 연결하여 출력합니다.
# [JP] 3つの文字列を '+' 演算子で1つに連結して出力します。
print(word1 + word2 + word3)

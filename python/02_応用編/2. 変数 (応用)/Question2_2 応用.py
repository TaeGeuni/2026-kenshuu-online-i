# [KR] 두 개의 변수 a와 b를 선언하고 값을 대입합니다.
# [JP] 2つの変数aとbを宣言し、値を代入します。
a = 10
b = 20

# [KR] 교환하기 전의 값을 출력합니다.
# [JP] 交換する前の値を出力します。
print(f"交換前: a = {a}, b = {b}")

# [KR] 파이썬의 강력한 기능인 '다중 할당(튜플 언패킹)'을 사용하여 임시 변수 없이 값을 교환합니다!
# [JP] Pythonの強力な機能である「複数代入（タプルアンパック）」を使用して、一時変数なしで値を交換します！
a, b = b, a

# [KR] 교환한 후의 값을 출력합니다.
# [JP] 交換した後の値を出力します。
print(f"交換後: a = {a}, b = {b}")

# [KR] 16진수 리터럴 접두사 '0x'를 사용하여 16진수 값 1A를 변수에 대입합니다.
# [JP] 16進数リテラルの接頭辞 '0x' を使用して、16進数値1Aを変数に代入します。
hex_num = 0x1A

# [KR] 파이썬의 print() 함수는 기본적으로 정수를 10진수로 변환하여 출력합니다.
# [JP] Pythonの print() 関数は、基本的に整数を10進数に変換して出力します。
print(f"10進数: {hex_num}")

# [KR] hex() 함수를 사용하면 정수를 다시 16진수 형태의 '문자열'로 변환할 수 있습니다.
# [JP] hex() 関数を使用すると、整数を再び16進数形式の「文字列」に変換できます。
print(f"16進数: {hex(hex_num)}")
